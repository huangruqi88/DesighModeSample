public class single {

    /**
     *
     * 单一职责原则：
     *              一个类中有且仅有一个引起他变化的原因
     *
     * 如果一个类承担的职责过多，就等于把这些职责耦合在一起，一个职责的变化可能会削弱或者抑制这个类的其他能力。这种耦合会导致脆弱的设置，当变化发生时，设计会遭到意向不到的破坏。
     *
     * 软件设计真正要做的许多内容，就是发现职责并把那些职责互相分离。如果在一个类中需要于一种动机，就需要职责分离
     */
}
